#include "3-calc.h"
/**
 * op_add - finds sum
 *
 * @a: first int
 * @b: second int
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - finds difference
 *
 * @a: first int
 * @b: second int
 * Return: difference
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplies two numbers
 *
 * @a: first int
 * @b: second int
 * Return: product
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divides two numbers
 *
 * @a: first int
 * @b: second int
 * Return: result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - finds modulo of a number
 *
 * @a: first int
 * @b: second int
 * Return: remainder
 */
int op_mod(int a, int b)
{
return (a % b);
}
